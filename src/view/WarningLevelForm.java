    public void loadData() throws SQLException{
        dtm=(DefaultTableModel)low_level_items.getModel();
        List<Item> low_items=itemcontroller.getAll();
        for(Item elem:low_items){
             if(elem.getQuantity()<elem.getWarning()){
             Object[] row={elem.getI_id(),elem.getDescription(),elem.getQuantity()};
             dtm.addRow(row);
             }
             if(low_items.size()==0)
                 JOptionPane.showMessageDialog(this, "No Items found","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }

